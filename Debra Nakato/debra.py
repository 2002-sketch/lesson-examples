# Python program to solve a math puzzle

first_number = 8
exponential_number = 3

power_result = first_number**exponential_number

print(power_result)

#Working with the another number
third_number = 45

#the quotient
quotient_division = third_number//first_number

print(quotient_division)

#Exact divison
exact_division = third_number/first_number

print(exact_division)

#finding the final value
final_value = power_result + quotient_division + exact_division

print(final_value)

#displaying the results
print(final_value)

print(type(final_value))