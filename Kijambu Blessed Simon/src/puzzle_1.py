# solving the math puzzle using python program
base_number = 3
exponential_number = 5

# power result
power_outcome = 3**5
print(power_outcome)

# working with another number
third_number = 20

# quotient division 
quotient_division = third_number//base_number
print(quotient_division) 

# exact division
exact_division = third_number/base_number
print(exact_division)

# finding the final number
final_number = exponential_number + quotient_division + exact_division
print(final_number)

# type of final value
print(type(final_number))  # the type is a float

